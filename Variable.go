package main

import "fmt"

func main() {
	var name string

	name = "Firli"
	fmt.Println(name)

	name = "Dito"
	fmt.Println(name)

	var sahabatku = "Adit,Enggar"

	fmt.Println(sahabatku)

	city := "Tangerang"
	fmt.Println(city)

	city = "KotaTangerang"
	fmt.Println(city)

	var (
		firstname = "Dito"
		lastname  = "sabana"
	)

	fmt.Println(firstname)
	fmt.Println(lastname)
}
